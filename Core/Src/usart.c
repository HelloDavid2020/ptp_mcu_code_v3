void bsp_uart_send_byte(USART_TypeDef *USARTx, uint8_t Byte)
{
  LL_USART_TransmitData8(USARTx, (Byte & 0xFF));

  while (!LL_USART_IsActiveFlag_TC(USARTx))
  {
  }
}


void bsp_uart_send_string(USART_TypeDef *USARTx, const char * str)
{
    while (*str != '\0')
    {
        LL_USART_TransmitData8(USARTx, (uint8_t)(*str));

        while (!LL_USART_IsActiveFlag_TC(USARTx))
        {
        }
        str++;
    }
}


void bsp_uart_send_packet(USART_TypeDef *USARTx, uint8_t *pData, uint16_t Size)
{
    while (Size>0)
    {
        LL_USART_TransmitData8(USARTx, (uint8_t)(*pData));

        while (!LL_USART_IsActiveFlag_TC(USARTx))
        {
        }
        Size--;
        pData++;
    }
}



void uart_rx_handler(void)
{
    uint8_t data = 0;

    if(LL_USART_IsActiveFlag_ORE(USART1))
    {
        printf("*** ORE ***\r\n");
        LL_USART_ClearFlag_ORE(USART1);
        data=LL_USART_ReceiveData8(USART1);
    }
    if(LL_USART_IsActiveFlag_IDLE(USART1))
    {
        LL_USART_ClearFlag_IDLE(USART1);
    }
    if(LL_USART_IsActiveFlag_RXNE(USART1))
    {
        data =LL_USART_ReceiveData8(USART1);
        callback_iap_rx(data);
    }

}

